//类名要和文件名保持一致

public class helloworld {
    public static void main(String[] args) {
        /*
        System.out 是一个对象
        println()  是方法
        */
        /*
        println 和 print 的区别
            print()方法在输出括号里指定的字符串后就结束操作，而不再添加回车，光标停留在字符串最后一个字符的右边
            println() 则是添加回车，光标停在下一行。
            */
        System.out.println("Hello World!\n");
    }
}

